#Working with Operators

#Arthematic Operators
x=int(input("Enter any value:"))
y=int(input("Enter any value:"))
print("The addition of X={} and y={} is {}".format(x,y,x+y))
print("The subtraction of X={} and y={} is {}".format(x,y,x-y))
print("The multiplication of X={} and y={} is {}".format(x,y,x*y))
print("The division of X={} and y={} is {}".format(x,y,x/y))
print("The modulo division of X={} and y={} is {}".format(x,y,x%y))
print("The floor division of X={} and y={} is {}".format(x,y,x//y))
print("The power of X={} and y={} is {}".format(x,y,x**y))