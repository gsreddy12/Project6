#Bitwise Operators

#Bitwise AND
a=10
b=20
print(bin(a))
print(bin(b))
print(a&b)

#Bitwise OR
print(bin(a))
print(bin(b))
print(a|b)

#Bitwise EXCLUSIVE OR
print(bin(a))
print(bin(b))
print(a^b)

#BITWISE COMPLEMENT
print(~a)
print(~b)

#~X=-X-1

#BINARY LEFT & RIGHT SHIFT
a=10
print(a<<2)
print(a>>2)

print(~True)
print(~False)
print(True<<2)
print(False<<2)
print(True>>2)
print(False>>2)
